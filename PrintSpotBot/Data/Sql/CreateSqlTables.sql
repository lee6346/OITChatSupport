CREATE TABLE [dbo].[ChatMessage](
	[Id]	INT IDENTITY (1, 1),
	[ConversationId] NVARCHAR(30) NOT NULL,
	[Sender] NVARCHAR(15) NOT NULL,
	[Text] NVARCHAR(200) NOT NULL,
	[Timestamp] DATETIME2(7) NOT NULL,
	CONSTRAINT [PK_Agent] PRIMARY KEY CLUSTERED ([Id] ASC)
)


CREATE TABLE [dbo].[Agent](
	[Id]	INT IDENTITY (1, 1),
	[UtsaId] NVARCHAR(15) NOT NULL,
	[Name] NVARCHAR(20) NOT NULL,
	[Connected] BIT NOT NULL DEFAULT(0),
	[ConversationId] NVARCHAR(30) NOT NULL,
	[RowVersion] ROWVERSION NULL,
	CONSTRAINT [PK_Agent] PRIMARY KEY CLUSTERED ([Id] ASC)
)

CREATE TABLE [dbo].[Thread](
	[Id]	INT IDENTITY (1, 1),
	[ConversationId] NVARCHAR(30) NOT NULL,
	[User] NVARCHAR(30) NOT NULL,
	[Timestamp] DATETIME2(7) NOT NULL
	CONSTRAINT [PK_Agent] PRIMARY KEY CLUSTERED ([Id] ASC)
)

CREATE TABLE [dbo].[LiveRequest](
	[Id]	INT IDENTITY(1, 1),
	[ConversationId] NVARCHAR(30) NOT NULL,
	[TimeStamp] DATETIME2(7) NOT NULL,
	[User] NVARCHAR(15) NOT NULL
	CONSTRAINT [PK_Agent] PRIMARY KEY CLUSTERED ([Id] ASC)
)